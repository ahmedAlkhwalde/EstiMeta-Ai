<?php
namespace App\Services;

class EstimationService
{
    
    /**
     * 1. حساب نقاط الوظيفة الخام (CFP) بناءً على جدول أوزان المحاضرة الرسمي
     */
    public function calculateFP($data) {
        // الأوزان الرسمية من جدول المحاضرة (تصنيف الـ Low كمثال افتراضي للتبسيط)
        $ilf_weight = 7;
        $eif_weight = 5;
        $ei_weight  = 3;
        $eo_weight  = 4;
        $eq_weight  = 3;

        $cfp = ($data['ilf'] * $ilf_weight) + 
               ($data['eif'] * $eif_weight) + 
               ($data['ei']  * $ei_weight)  + 
               ($data['eo']  * $eo_weight)  + 
               ($data['eq']  * $eq_weight);

        // حساب معامل تعديل التعقيد النسبي (RCAF) من الخصائص الـ 14
        // في مشروع الصيدلية، نثبت التأثير عند درجة (3 - تأثير متوسط) لكل خاصية ليعطي نتيجة واقعية
        $tdi = 14 * 3; 

        // المعادلة الرسمية من المحاضرة: FP = CFP * (0.65 + 0.01 * RCAF)
        return $cfp * (0.65 + (0.01 * $tdi));
    }

    /**
     * 2. حساب نقاط حالات الاستخدام (UCP) بناءً على أوزان وقوانين المحاضرة الرسمية
     */
    public function calculateUCP($data) {
        // UUCP = UAW + UUCW (المعادلة الرسمية من المحاضرة)
        $uucp = $data['uaw'] + $data['uucw'];

        // حساب TCF (عامل التعقيد التقني) المعتمد في السلايدات: TCF = 0.6 + (0.01 * TFactor)
        // افترضنا درجة التأثير متوسطة (3) لجميع العوامل الـ 13، مضروبة بأوزانها الرسمية
        $t_factor = (2*3) + (1*3) + (1*3) + (1*3) + (1*3) + (0.5*3) + (0.5*3) + (2*3) + (1*3) + (1*3) + (1*3) + (1*3) + (1*3); // الإجمالي = 43.5
        $tcf = 0.6 + (0.01 * $t_factor);

        // حساب ECF (عامل البيئة) المعتمد في السلايدات: ECF = 1.4 + (-0.03 * EFactor)
        // واعتبرنا خبرة فريق الطلاب (3 - متوسطة) مضروبة بالأوزان الرسمية للـ 8 عوامل بيئية
        $e_factor = (1.5*3) + (0.5*3) + (1*3) + (0.5*3) + (1*3) + (2*3) + (-1*3) + (-1*3); // الإجمالي = 13.5
        $ecf = 1.4 + (-0.03 * $e_factor);

        // المعادلة النهائية من المحاضرة: UCP = UUCP * TCF * ECF
        return $uucp * $tcf * $ecf;
    }

    /**
     * 3. تقدير الجهد التناقشي (Effort)
     * بناءً على المحاضرة: للمشاريع الصغيرة/الطلابية التي لا تتطلب جودة عالية، نعتمد PF = 12 ساعة/نقطة
     */
    public function estimateEffort($ucp) {
        return $ucp * 12; 
    }
}